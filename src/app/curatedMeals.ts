function slugify(s: string): string {
  return s.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '');
}

function pickDifficulty(idx: number): 'Easy' | 'Medium' | 'Hard' {
  const m = idx % 5;
  if (m === 0) return 'Medium';
  if (m === 1) return 'Hard';
  return 'Easy';
}

function pickTime(minutes?: number, type?: string): number {
  if (minutes) return minutes;
  if (type === 'breakfast') return 10 + ((Math.random() * 15) | 0);
  return 20 + ((Math.random() * 30) | 0);
}

function pickCalories(cal?: number, type?: string): number {
  if (cal) return cal;
  if (type === 'breakfast') return 250 + ((Math.random() * 200) | 0);
  if (type === 'lunch') return 350 + ((Math.random() * 250) | 0);
  return 420 + ((Math.random() * 280) | 0);
}

